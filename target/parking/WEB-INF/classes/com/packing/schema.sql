CREATE TABLE USER(
	EMAIL VARCHAR(50) NOT NULL primary key,
    FIRSTNAME VARCHAR(50) NOT NULL,
    LASTNAME VARCHAR(50) NOT NULL,
    DEPARTMENT VARCHAR(50) NOT NULL,
    CARNUMBER VARCHAR(50) NOT NULL,
    PWORD VARCHAR(50) NOT NULL
);

CREATE TABLE PARKING_SECTION(
	SECTION_ID VARCHAR(3) NOT NULL PRIMARY KEY,
    SECTION_NAME VARCHAR(50) NOT NULL,
    NUMBER_OF_PARKING_SLOTS INT(3)
);

CREATE TABLE PARKING_LOT(
	PARKING_LOT_ID VARCHAR(3) NOT NULL PRIMARY KEY,
    PARKING_LOT_NAME VARCHAR(50) NOT NULL
);


CREATE TABLE USERS_PARKING_LOT(
	PARKING_LOT_ID VARCHAR(3) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL,
    PARKING_STATUS INT(1),
    FOREIGN KEY (PARKING_LOT_ID) REFERENCES PARKING_LOT(PARKING_LOT_ID),
    FOREIGN KEY (EMAIL) REFERENCES USER(EMAIL),
    PRIMARY KEY(PARKING_LOT_ID, EMAIL)
    
);

